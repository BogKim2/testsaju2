# 시지 조견표: 시간대 → 지지 인덱스
# 23:00-00:59 → 자(0), 01:00-02:59 → 축(1), ...
def get_siji_index(hour: int) -> int:
    """시간(0~23)을 지지 인덱스(0~11)로 변환"""
    return (hour + 1) // 2 % 12

